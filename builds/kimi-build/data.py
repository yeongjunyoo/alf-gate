def build_cases():
    """과거 문의 로그를 유형별로 집계하고 상위 유형에서 케이스를 자동 합성한 결과를 반환한다."""
    tag_counts = Counter({tag: len(msgs) for tag, msgs in CHAT_LOGS.items()})
    total = sum(tag_counts.values())
    cases = []
    for tpl in CASE_TEMPLATES:
        c = dict(tpl)
        c["persona_name"] = c["persona"].split("(")[0]
        c["order"] = ORDERS.get(c["id"])
        c["log_count"] = tag_counts.get(c["tag"], 0)
        cases.append(c)
    covered_tags = {c["tag"] for c in cases}
    covered = sum(cnt for tag, cnt in tag_counts.items() if tag in covered_tags)
    uncovered = [{"tag": tag, "count": cnt} for tag, cnt in tag_counts.items() if tag not in covered_tags]
    coverage = {
        "total_logs": total,
        "covered_logs": covered,
        "rate": covered / total if total else 0,
        "by_tag": [{"tag": tag, "count": cnt, "covered": tag in covered_tags} for tag, cnt in tag_counts.most_common()],
        "uncovered": uncovered,
    }
    return cases, coverage
